class AppThemes {
  static ThemeData lightTheme(ColorSeed colorSeed) {
    return ThemeData(
      colorSchemeSeed: colorSeed.color,
      useMaterial3: true,
      brightness: Brightness.light,
    );
  }

  static ThemeData darkTheme(ColorSeed colorSeed) {
    return ThemeData(
      colorSchemeSeed: colorSeed.color,
      useMaterial3: true,
      brightness: Brightness.dark,
    );
  }
}

